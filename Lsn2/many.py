# Множества

colors = {'red', 'green', 'blue'}
print(colors)

colors.add('gray')  # Добавление
print(colors)

colors.remove('red')  # Удаление
print(colors)

colors.discard('red')  # Проверяет и если есть, удаляет без ошибки
print(colors)

colors.clear()  # Очищение всего множества
print(colors)

q = set()  # Создание2 множества
q.add('Привет')
print(q)

